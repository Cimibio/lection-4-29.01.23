void PrintImage(int[,] arg)
{
    for (int i = 0; i < arg.GetLength(0); i++)
    {
        for (int j = 0; j < arg.GetLength(1); j++)
        {
            //Console.Write($" {arg[i, j]} ");
            if (arg[i,j] == 0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)    //пример рексурссии, когда метод вызывает сам себя
{
